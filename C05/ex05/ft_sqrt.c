int	ft_sqrt(int nb)
{
	long int	sqrt;

	sqrt = 0;
	while (sqrt <= nb)
	{
		if (sqrt * sqrt == nb)
			return (sqrt);
		if (sqrt * sqrt > nb)
			return (0);
		sqrt++;
	}
	return (0);
}
